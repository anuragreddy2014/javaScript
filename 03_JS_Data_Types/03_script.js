// String
let name = 'John';
console.log(`Value => ${name} , Type => ${typeof name}`);

// number
let age = 40;
console.log(`Value => ${age} , Type => ${typeof age}`);

// boolean
let isITEmployee = true;
console.log(`Value => ${isITEmployee} , Type => ${typeof isITEmployee}`);

// Undefined
let justVar;
console.log(`Value => ${justVar} , Type => ${typeof justVar}`);

// null
let empSalary = null;
console.log(`Value => ${empSalary} , Type => ${typeof empSalary}`);

// Reassignment
let abc; // undefined
abc = 'test'; // String
abc = 100; // Number
abc = true; // Boolean
abc = [10,20,30]; // Array
abc = {name : 'John'}; // Object
console.log(`Value => ${abc} , Type => ${typeof abc}`);


